public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 354.327423f;
        System.out.printf("Formatted number is %.2f\n", a);

        System.out.printf("Pie: %.3f\n", Math.PI);

        int age = 20;
        String name = "Rohan Rahim";
        System.out.printf("Hello my name is %s and I'm %d years old.\n", name, age);

//        Placeholders
//        There are many format specifiers we can use. Here are some common ones:
//
//                %c - Character
//                %d - Decimal number (base 10)
//                %e - Exponential floating-point number
//                %f - Floating-point number
//                %i - Integer (base 10)
//                %o - Octal number (base 8)
//                %s - String
//                %u - Unsigned decimal (integer) number
//                %x - Hexadecimal number (base 16)
//                %t - Date/time
//                %n - Newline
    }
}
